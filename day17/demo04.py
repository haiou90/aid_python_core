"""
    函数式编程 - 语法
        理论支柱：
            将函数赋值给变量,通过变量调用函数
        应用：
            将函数赋值给参数,通过参数调用函数
            （价值：通过参数调用函数,可以将(核心)逻辑注入到函数中.）
"""


def func01():
    print("func01执行了")

# 将函数赋值给变量
a = func01
# 通过变量调用函数
a()

def func02():
    print("func02执行了")

# 价值:通过参数调用函数,可以将(核心)逻辑注入到函数中.
def func03(a):
    print("func03执行了")
    a()# 执行?

func03(func01)
func03(func02)