"""
    函数参数
        实际参数
            1. 位置实参:实参按照位置与形参进行对应
                3.序列实参：使用*号拆分序列,将元素与形参对应
            2. 关键字实参：实参按照名称与形参进行对应
                4.字典实参：使用**号拆分字典,将键值对与形参对应
"""


def func01(p1, p2, p3):
    print(p1)
    print(p2)
    print(p3)


list01 = [1, 2, 3]
str02 = "123"
tuple03 = (1, 2, 3)
func01(*list01)  # 拆


dict04 = {"p1": 1, "p2": 2, "p3": 3}
func01(**dict04)
