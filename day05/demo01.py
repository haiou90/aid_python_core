"""
    通用操作
        数学运算
        成员运算
"""
# 1.
# + 用于【拼接】两个容器
name = "悟空"
name += "八戒"
print(name)  # "悟空八戒"

# * 【重复】生成容器元素
name *= 3
print(name)

# 比较 依次比较两个容器中元素,一但不同则返回比较结果。
print("悟空" =="八戒")
print("悟空" !="八戒")

# 2.
# 元素　在　容器中
# 注意：顺序、连续
print("悟空" in "我是花果山水帘洞孙悟空")
# 元素　在　容器中
print("悟空" not in "我是花果山水帘洞孙悟空")
