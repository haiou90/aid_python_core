"""
    浅拷贝内存图
"""
list01 = [10, [20, 30], [40, 50]]
# list02 得到的是变量list01存储的列表地址
list02 = list01
# 通过list02修改列表第一个元素,再通过list01访问受影响
list02[0] = "十"
list02[1][0] = "二十"
# list03 得到的列表浅拷贝(一层)出来的新列表
list03 = list01[:]
# 修改第一层(不影响拷贝前的数据list01)
list03[0] = "a"
# 修改第二层(影响拷贝前的数据list01)
list03[1][1] = "b"
